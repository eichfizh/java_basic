package com.eichfizh;

public class Main {
    public static void main(String[] args) {
        // Contoh penggunaan if
        int a = 5;

        System.out.println("Nilai = " + a);

        /*
         * Ini adalah contoh if
         * if (ekspresi){
         *   statement
         * }
        */

        /*
         * Ini adalah contoh if-else
         * if (ekspresi){
         *   statement
         * } else {
         *   statement
         * }
        */

        // ini contoh percabangannya
        if (a == 10) {
            System.out.println("Ini jalur true");
        } else {
            System.out.println("Ini adalah jalur false");
        }

        System.out.println("Selesai");
    }
}
